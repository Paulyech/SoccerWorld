The attackers begin with the ball. <br>
Place a keeper in goal. <br>
<span class="font-weight-bold">  Drill Instructions</span>  <br>

Instruct the players to exploit their numerical advantage and finish with a shot on goal. <br>
Attackers should move the ball looking for the open player and holes in the defense. <br>
Defenders should attempt to break-down the attack and to gain experience in how to cover a man-down situation. <br>
Defensive players should complete a pass to the coach to end the attack. <br>
The coach should then give the ball back to the attackers to build the attack again. <br>

<span class="font-weight-bold">  Drill Variations</span>  <br>

Increase the number of attackers (or decrease the number of defenders) if scoring chances are infrequent. <br>
Increase the number of defenders (or decrease the number of attackers) if scoring chances are too frequent <br>

<span class="font-weight-bold">  Drill Coaching Points</span>  <br>


<i>Offensive:</i> 

Quickly switch the point of attack to off-balance the defense. <br>
Play quick both physically (1 and 2 touch) and mentally (think ahead). <br>
Make sure the players are attacking at the appropriate times <br>

<i>Defensive:</i> 

Contain the play and keep the ball in front of the defense. <br>
Early pressure and do not allow space behind the defenders. <br>
