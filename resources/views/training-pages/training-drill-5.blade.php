Players line up at the last with a ball each. <br>
<span class="font-weight-bold"> Drill Instructions</span>  <br>

       The first player in line dribbles around the outside of the cones in a zig-zag pattern.  <br>
       When the dribbling player reaches the end, they must sprint the distance of the zig-zag course and return to the line. <br>
       Let the players go through the course a few times to get comfortable with their touch. <br>
       Now as the players are moving to their right side, they must dribble normal. <br>
       However, as the players move to the left side they must roll the ball with the soul of the right foot. <br>
       Change the pattern to move normally when moving to the left, but roll the ball with the soul of their left foot as they move to the right. <br>

<span class="font-weight-bold">  Drill Variations</span>  <br>               
            Additional dribbling skills can be added such as: <br>
           Dribbling in a complete circle around each cone <br>
           Moving the ball through the pattern with the soul of the foot only. <br>
<span class="font-weight-bold">  Drill Coaching Points</span>  <br> 
   

       Controlled touches near the dribbling player. <br>
       Dribbling with the outside of the foot, inside of the foot, and soul of the foot <br>
       Dribbling with speed <br>


