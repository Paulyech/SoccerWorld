Each group should line up about 6 yards from the passing window. <br>
                The play in front of one of the lines should start with the ball. <br>
          <span class="font-weight-bold">  Drill Instructions</span>  <br>   
            
            
                The first player in line passes the ball through the window to the first player in the other line. <br>
                The passing player then follows their pass to the back of the other line by sprints outside the window. <br>
                The receiving player then takes a touch then plays the ball back through the window to the next player in the other line. <br>
                The passing player then follows their pass to the back of the other line.
                Repeat. <br>
        <span class="font-weight-bold">  Drill Variations</span>  <br>        
               
                 Pass in one-touch <br>
         <span class="font-weight-bold"> Drill Coaching Points</span>  <br> 
            

                Clean first touch <br>
                Toe up, ankle locked <br>
                Strike the ball smoothly with the inside of the foot <br>
                Good pace on the ball <br>
                Focus on accuracy between the cones

             
          
